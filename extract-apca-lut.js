// extract-apca-lut.js
import { readFileSync, writeFileSync, readdirSync } from "fs";

// Find whatever LUT file exists in the apca-w3 data folder
const dataDir = "./node_modules/apca-w3/data";
const lutFile = readdirSync(dataDir).find(f => f.startsWith("LUT-"));
if (!lutFile) { console.error("No LUT file found in apca-w3/data!"); process.exit(1); }

const raw = readFileSync(`${dataDir}/${lutFile}`, "utf8");
console.log(`Parsing ${lutFile}...`);

// Find the fontMatrixG tab-delimited section — it starts after
// "const fontMatrixG = [" and the header line "Lc\t100\t200..."
const sectionMatch = raw.match(/const fontMatrixG = \[[\s\S]*?Lc\t100\t200\t300\t400\t500\t600\t700\t800\t900\n([\s\S]*?)\n\n/);
if (!sectionMatch) { console.error("Could not find fontMatrixG section!"); process.exit(1); }

const tableLines = sectionMatch[1].trim().split("\n");
const weights = [100,200,300,400,500,600,700,800,900];
const matrix = {};
const lcKeys = [];

for (const line of tableLines) {
  const cols = line.split("\t");
  const lc = parseFloat(cols[0]);
  if (isNaN(lc)) continue;
  lcKeys.push(lc);
  matrix[lc] = {};
  weights.forEach((w, i) => {
    const val = parseFloat(cols[i + 1]);
    matrix[lc][w] = isNaN(val) ? null : val;
  });
}

// Sort lcKeys low to high
lcKeys.sort((a, b) => a - b);

const output = `// ─── APCA FONT MATRIX ────────────────────────────────────────────────────────
// Auto-generated by extract-apca-lut.js — do not edit manually.
// Source: node_modules/apca-w3/data/${lutFile}
// Last generated: ${new Date().toISOString()}

export const fontMatrixG = ${JSON.stringify(matrix, null, 2)};
export const fontMatrixLcKeys = ${JSON.stringify(lcKeys)};
export const fontMatrixWeightKeys = [100,200,300,400,500,600,700,800,900];

export function minSizeForLc(lcValue, weightKey) {
  const keys = fontMatrixLcKeys;
  let lowerLc = null, upperLc = null;
  for (let i = 0; i < keys.length; i++) {
    if (keys[i] <= lcValue) lowerLc = keys[i];
    if (keys[i] >= lcValue && upperLc === null) upperLc = keys[i];
  }
  if (lowerLc === null) lowerLc = keys[0];
  if (upperLc === null) upperLc = keys[keys.length - 1];

  const wKeys = fontMatrixWeightKeys;
  let wLo = wKeys[0], wHi = wKeys[wKeys.length - 1];
  for (const w of wKeys) { if (w <= weightKey) wLo = w; }
  for (const w of wKeys) { if (w >= weightKey) { wHi = w; break; } }

  function interpWeight(lcRow, wTarget) {
    const row = fontMatrixG[lcRow];
    if (!row) return null;
    let lo = wLo, hi = wHi;
    while (lo >= wKeys[0] && row[lo] == null) lo -= 100;
    while (hi <= wKeys[wKeys.length-1] && row[hi] == null) hi += 100;
    const vLo = row[lo], vHi = row[hi];
    if (vLo == null && vHi == null) return null;
    if (vLo == null) return vHi;
    if (vHi == null) return vLo;
    if (lo === hi) return vLo;
    const t = (wTarget - lo) / (hi - lo);
    return vLo + t * (vHi - vLo);
  }

  const sizeAtLower = interpWeight(lowerLc, weightKey);
  const sizeAtUpper = interpWeight(upperLc, weightKey);
  if (sizeAtLower == null && sizeAtUpper == null) return null;
  if (sizeAtLower == null) return sizeAtUpper;
  if (sizeAtUpper == null) return sizeAtLower;
  if (lowerLc === upperLc) return sizeAtLower;
  const t = (lcValue - lowerLc) / (upperLc - lowerLc);
  return sizeAtLower + t * (sizeAtUpper - sizeAtLower);
}

export function minLcForSize(sizePx, weightKey) {
  const sizes = fontMatrixLcKeys.map(lc => ({ lc, size: minSizeForLc(lc, weightKey) }))
    .filter(item => item.size !== null && item.size !== 999 && item.size !== 777);
  if (sizes.length === 0) return 100;
  let lower = sizes[sizes.length - 1], upper = sizes[0];
  for (let i = 0; i < sizes.length; i++) {
    if (sizes[i].size >= sizePx) upper = sizes[i];
    if (sizes[i].size <= sizePx) { lower = sizes[i]; break; }
  }
  if (lower.lc === upper.lc) return lower.lc;
  const t = (sizePx - lower.size) / (upper.size - lower.size);
  return lower.lc + t * (upper.lc - lower.lc);
}
`;

writeFileSync("./apca-lookup.js", output);
console.log('apca-lookup.js generated successfully from ' + lutFile);